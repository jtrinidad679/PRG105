user_number = int(input("Enter a number between 1 and 7: "))

if user_number == 1:
    print("Monday")
elif user_number == 2:
    print("Tuesday")
elif user_number == 3:
    print("Wednesday")
elif user_number == 4:
    print("Thursday")
elif user_number == 5:
    print("Friday")
elif user_number == 6:
    print("Saturday")
elif user_number == 7:
    print("Sunday")
else:
    print("Error: That number is not valid")
